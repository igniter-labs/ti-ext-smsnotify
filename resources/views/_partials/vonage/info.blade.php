    <h4>Setting up the vonage channel</h4>
    <ol class="pl-3 mb-0">
        <li>First, go to the <a href="https://dashboard.vonage.com/sign-up" target="_blank">Vonage's website</a>
            and sign up for a Vonage account if you haven't already.
        </li>
        <li>Under your account's profile, click on the settings option and copy the API key and API secret from the
            corresponding
            field.
        </li>
        <li>Enter the API key and API secret below, and click on Save Changes.
        </li>
        <li>That's it! Vonage SMS channel has been configured!</li>
    </ol>
