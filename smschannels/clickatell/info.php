<div class="alert text-reset">
    <h4>Setting up the clickatell channel</h4>
    <ol class="pl-3">
        <li>First, go to <a href="https://www.clickatell.com/signup/" target="_blank">Clickatell's website</a> and
            create an account if you haven't
            already.
        </li>
        <li>Once you've signed up you will receive an API ID.</li>
        <li>Go to <b>System > Settings > SMS Channels Settings</b> and click on Clickatell Channel.</li>
        <li>Enter the API ID you received by signing up to Clickatell, and click on Save Changes.</li>
        <li>That's it! Clickatell SMS channel has been configured!</li>
    </ol>
</div>